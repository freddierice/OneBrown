#import "OneBrownCommon.h"

@implementation OneBrownCommon

+ (UIColor *)transparentGrayColor {
    return [UIColor colorWithWhite:0.4f alpha:0.9f];
}

+ (UIColor *)transparentWhiteColor {
    return [UIColor colorWithWhite:1.0f alpha:0.9f];
}

+ (UIColor *)brightOrangeColor {
    return [UIColor colorWithRed:211.0f / 255.0f green:71.0f / 255.0f blue:42.0f / 255.0f alpha:1.0f];
}

@end
